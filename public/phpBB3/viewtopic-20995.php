		<td colspan="2"><hr class="sep" />Hi Grilgamesh, UtnapishTim here, get it? Epic reference, get it?<br /><br />Of course we did it at home, to suggest that we could achieve anything in a day is madness - it took forever AND a day, which is why there's no artwork. It was all done using Thom's homemade studio facilities at Dildano HQ, on Ubuntu, on Linux, on a computer, on the floor. <br /><br />We would play the songs with guitars in headphone amps and record the drums with 6 mics, often DI'ing the bass and using that take too. All guitars were then overdubbed using Thom's wonderful selection of microphones and then vox and synths were par-boiled and roasted in sumo-fat. We didn't do too much editing and used no software effects as Thom built his own compressor. It was a freaking pleasure and I'm looking forward to doing the next album, but I don't think we'll use Linux again. Oh yeah, and the interludes went out and back through a tape player for added subtractions.<br /><br />Perhaps we should have some sort of Artwork competition?
		
